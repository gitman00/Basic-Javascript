// variables in javascript can be declared by using built-in syntax which is "Var"/"let"
// We can declare variables in javascript using var and let 
// ====================== Example =========================  

// using Var
let Name = "Alex";
console.log(Name);

// Using Let
let AName = "Alex";
console.log(AName);

// There is not enough difference between var and let . The only differencs is that,
// var is function scoped and let is blocked scoped.

